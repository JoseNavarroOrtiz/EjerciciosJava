/**
 * Muestra la resta desde 320 hasta 160 de 20 en 20 con do-while
 *
 * @author 
 */

public class resta20En20DoWhile{
  public static void main(String[] args) {
  
  System.out.println("Este programa resta desde 320 hasta 160 de 20 en 20");
  int numero =320 ;
  
  
   do{
    System.out.println(numero);
    numero = numero-20; 

    
    }while(numero >=160);
  
        
    
  }
}
