/**
 * Restanlos numeros desde 320 hasta 160 de 20 en 20
 *
 * @author 
 */

public class resta20En20{
  public static void main(String[] args) {
  
  System.out.println("Este programa restan los numeros desde 320 hasta 160 de 20 en 20");
  
  for(int i = 320;(i >= 160); i= i-20){
    
    System.out.println(i);
    
  }
        
    
  }
}
